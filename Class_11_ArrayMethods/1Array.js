let eids = [101,102,103,104]
console.log(eids.length)

eids.push(105,106) //Adding element end of array
console.log(eids)

eids.pop()  //Removes last element of array
console.log(eids)

eids.unshift(100) // Adding element in begening or starting of the array
console.log(eids)

eids.shift()
console.log(eids) // Removes first element of array

eids.reverse()
console.log(eids) // Makes the array in reverse order